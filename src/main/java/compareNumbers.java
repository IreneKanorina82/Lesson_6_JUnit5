
public class compareNumbers {
    public static void compareNumbers(int a, int b) {
        if (a < b) {
            System.out.println(a + " < " + b);
        }
        if (a > b) {
            System.out.println(a + " > " + b);
        }
        if (a == b) {
            System.out.println(a + " = " + b);
        }
    }
}
